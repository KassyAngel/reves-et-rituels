// Normalise : minuscules + supprime accents + ponctuation
function normalize(text: string): string {
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "") // supprime les accents
    .replace(/[^a-z0-9\s]/g, " ");   // supprime ponctuation
}

// Tronque un mot à sa racine (5 premiers caractères)
function stem(word: string): string {
  return word.length > 5 ? word.slice(0, 5) : word;
}

export const interpretDream = (text: string, lang: 'fr' | 'en') => {
  const normalizedText = normalize(text);
  const words = normalizedText.split(/\s+/);
  const stemmedWords = words.map(stem);

  const keywords = dreamKeywords[lang];
  const matches = [];

  for (const category of keywords) {
    const matched = category.keywords.some(kw => {
      const normalizedKw = normalize(kw);
      const stemmedKw = stem(normalizedKw);

      // Match exact sur le texte normalisé
      if (normalizedText.includes(normalizedKw)) return true;

      // Match sur les racines des mots
      if (stemmedWords.some(w => w === stemmedKw)) return true;

      return false;
    });

    if (matched) matches.push(category);
  }

  if (matches.length === 0) return null;
  if (matches.length === 1) return matches[0];

  return {
    icon: matches.map(m => m.icon).slice(0, 3).join(" "),
    interpretation: matches.map(m => m.interpretation).join("\n\n")
  };
};